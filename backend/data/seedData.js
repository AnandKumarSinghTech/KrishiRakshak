async function seedDatabase() {
  try {
    
    const MONGO_URI = process.env.MONGO_URI || 'mongodb://localhost:27017/kisanrakshak';
    await mongoose.connect(MONGO_URI);
    console.log('✅ Connected to MongoDB');
    console.log(`📍 Database: ${MONGO_URI}\n`);

    const deleted = await Disease.deleteMany({});
    console.log(`🗑️  Cleared existing data: ${deleted.deletedCount} documents deleted`);

    const inserted = await Disease.insertMany(diseases);
    console.log(`✅ Inserted ${inserted.length} disease records`);

    console.log('\n📊 Seeded dataset summary:');
    const crops = [...new Set(diseases.map(d => d.cropName))];
    for (const crop of crops) {
      const count = diseases.filter(d => d.cropName === crop).length;
      console.log(`   ${crop.padEnd(10)} → ${count} diseases`);
    }

    console.log('\n🌾 Database seeded successfully!');
    console.log('   You can now start the server: node server.js');

  } catch (error) {
    console.error('\n❌ Seeding failed:', error.message);
    console.error('💡 Make sure MongoDB is running first!');
  } finally {
    
    await mongoose.connection.close();
    console.log('\n🔌 Database connection closed.');
    process.exit(0);
  }
}

seedDatabase();
